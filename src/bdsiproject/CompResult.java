    public CompResult(java.awt.Frame parent, boolean modal, List<String> result) {
        super(parent, modal);
        try {
            if (result.isEmpty()) {
                JOptionPane.showMessageDialog(parent, "No existen archivos PATHWAY en comun.", "JavaKEGG 1.0: Error", JOptionPane.ERROR_MESSAGE);
                this.dispose();
            } else {
                pc = new PythonConnector();
                UIManager.setLookAndFeel(UIManager.getSystemLookAndFeelClassName());
                initComponents();
                this.setLocationRelativeTo(null);
                rdOpt1.setText(result.get(0));
                rdOpt2.setText(result.get(1));
                prefix = result.get(0).split("- ")[1];
                for (int i = 2; i < result.size(); i++) {
                    String string = result.get(i);
                    for (Object item : pc.getItems("map" + string, 0)) {
                        //System.out.println(item);
                        cboItemsRes.addItem(string + ": " + (String) item);
                    }
                }
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

